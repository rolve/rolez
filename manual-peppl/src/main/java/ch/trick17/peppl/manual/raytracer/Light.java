package ch.trick17.peppl.manual.raytracer;

import ch.trick17.peppl.lib.immutable.ImmutableObject;

public class Light extends ImmutableObject {
    public final ImmutableVec pos;
    public final double brightness;
    
    public Light(final double x, final double y, final double z,
            final double brightness) {
        this.pos = new ImmutableVec(x, y, z);
        this.brightness = brightness;
    }
}
